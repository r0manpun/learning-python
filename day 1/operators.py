
# Numbers in Python
# Integers - whole numbers
# Floats - floating point numbers which are negative or positive containing one or more decimal
# Complex Numbers - used in electrical engineering

# Operators in Python
# - An operator is a symbol of the programming language able to operate on values
# Arithmetic Operators: + - * / // ** %
# Assignment Operators: = += -= *= /=
# Comparison Operators: == != > >= < <=
# Identity Operators: 'is' 'is not'
# Logical Operators: 'and' 'or' 'not'

# Arithmetic Operators
print(5 - 5) # 0
print(5 + 5) # 10
print(8 / 4) # 2
print(11 // 5) # 2 - floor/integer division operator
print(5 * 4) # 20
print(2 ** 3) # 8 - exponential or raising to a power operator
print(20 % 3) # 2 -  modulus or mode operator, returns remainders


# Order of Operators
# 1. Exponentiation (**)
# 2. Multiplication (*) and division (/)
# 3. Addition (+) and subtraction (-)

