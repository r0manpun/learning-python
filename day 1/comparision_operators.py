# Comparison Operators

# Equality/Equal to operator (==)
print(7 ==8 ) # False
print('abc'== 'abc') # True

# Inequality/Not equal to operator (!=)
print (7!= 7) # False
print('a' != 'A') # True

# Greater than operator (>)
a, b = 7, 9
print(a>b) # False

# Greater than or Equal to (>=)
print(b>= 9) # True

# Less than (<)
print(a<b) # True

# Less than or Equal to
print(a*3 <=100) # True
